package trip.vo;

//채팅정보 데이터 VO
public class chatVO 
{
	private String cno;     //채팅번호
	private String cnote;   //채팅내용
	private String cwdate;  //채팅작성일
	private	String uno;     //회원번호
	private	String unick;   //닉네임
	
	public String getCno()    {	return cno;	   }
	public String getCnote()  {	return cnote;  }
	public String getCwdate() {	return cwdate; }
	public String getUno()    {	return uno;	   }
	public String getUnick()  {	return unick;  }
	
	public void setCno(String cno)       { this.cno = cno;	     }
	public void setCnote(String cnote)   { this.cnote = cnote;	 }
	public void setCwdate(String cwdate) { this.cwdate = cwdate; }
	public void setUno(String uno)       { this.uno = uno;	     }
	public void setUnick(String unick)   { this.unick = unick;	 }
	
	
}
