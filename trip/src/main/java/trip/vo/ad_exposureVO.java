package trip.vo;

public class ad_exposureVO 
{
	// field
	private String exno;           //광고노출 번호
	private String exposure_date;  //노출 날짜
	// foreign key
	private	String advertno;   // 광고번호
	private	String bno;        // 게시물번호
	private	String uno;        // 회원번호
	
	// getter
	public String getExno()          { return exno;          }
	public String getExposure_date() { return exposure_date; }
	// foreign key
	public String getAdvertno()      { return advertno;      }
	public String getBno()           { return bno;           }
	public String getUno()           { return uno;           }
	
	
	
	// setter
	public void setExno(String exno)                     { this.exno = exno;                     }
	public void setExposure_date(String exposure_date)   { this.exposure_date = exposure_date;   }
	// foreign key
	public void setAdvertno(String advertno)             { this.advertno = advertno;             }
	public void setBno(String bno)                       { this.bno = bno;                       }
	public void setUno(String uno)                       { this.uno = uno;                       }
	
}
